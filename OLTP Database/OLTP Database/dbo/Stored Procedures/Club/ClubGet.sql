CREATE PROCEDURE [dbo].[ClubGet]
	@ClubId INT = NULL
AS
BEGIN
	SET NOCOUNT ON
    SET XACT_ABORT ON

	BEGIN TRAN
		BEGIN TRY
			IF (@ClubId IS NULL)
			BEGIN
				SELECT * FROM [dbo].[Clubs]
			END
			ELSE
			BEGIN
				SELECT TOP 1 * FROM [dbo].[Clubs] WHERE [Id] = @ClubId
			END
			COMMIT
		END TRY
		BEGIN CATCH
			IF (@@ERROR <> 0)
			BEGIN 
				ROLLBACK
				;THROW
			END
			RETURN NULL
		END CATCH

END
