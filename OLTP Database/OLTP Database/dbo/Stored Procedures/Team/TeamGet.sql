CREATE PROCEDURE [dbo].[TeamGet]
	@TeamId INT = NULL
AS
BEGIN
	SET NOCOUNT ON
    SET XACT_ABORT ON

	BEGIN TRAN
		BEGIN TRY
			IF (@TeamId IS NULL)
			BEGIN
				SELECT * FROM [dbo].[Teams]
			END
			ELSE
			BEGIN
				SELECT TOP 1 * FROM [dbo].[Teams] WHERE [Id] = @TeamId
			END
			COMMIT
		END TRY
		BEGIN CATCH
			IF (@@ERROR <> 0)
			BEGIN 
				ROLLBACK
				;THROW
			END
			RETURN NULL
		END CATCH

END
